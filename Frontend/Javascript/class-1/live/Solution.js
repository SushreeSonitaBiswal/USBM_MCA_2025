// Define the prices for food and drink
let foodPrice = 140.00;  // Example food price
let drinkPrice = 100.00;  // Example drink price

// Calculate the total bill
let totalBill = foodPrice + drinkPrice;

// Calculate the tip (10% of the total bill)
let tip = totalBill * 0.10;

// Calculate the final amount (total bill + tip)
let finalAmount = totalBill + tip;

// Display the final amount in the console
console.log(`The total bill (including a 10% tip) is: ${finalAmount}`);//264

